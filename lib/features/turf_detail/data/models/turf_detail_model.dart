import 'package:cloud_firestore/cloud_firestore.dart';

import '../../domain/entities/turf_detail.dart';

/// Firestore DTO for TurfDetail.
class TurfDetailModel extends TurfDetail {
  const TurfDetailModel({
    required super.id,
    required super.name,
    required super.description,
    required super.location,
    required super.address,
    required super.city,
    required super.latitude,
    required super.longitude,
    required super.images,
    required super.rating,
    required super.reviewCount,
    required super.pricePerHour,
    super.priceWeekday,
    super.priceWeekend,
    required super.sports,
    required super.amenities,
    required super.ownerUid,
    required super.ownerName,
    required super.ownerPhone,
    required super.openTime,
    required super.closeTime,
    super.slotDurationMinutes,
    super.numberOfGrounds,
    super.sportPricing,
    super.isVerified,
    super.isActive,
    super.contactPhone,
  });

  factory TurfDetailModel.fromFirestore(DocumentSnapshot doc) {
    final d = doc.data() as Map<String, dynamic>;
    return TurfDetailModel(
      id: doc.id,
      name: d['name'] as String? ?? '',
      description: d['description'] as String? ?? '',
      location: d['location'] as String? ?? '',
      address: d['address'] as String? ?? '',
      city: d['city'] as String? ?? 'Nashik',
      latitude: (d['latitude'] as num? ?? 20.0).toDouble(),
      longitude: (d['longitude'] as num? ?? 73.7).toDouble(),
      images: List<String>.from(d['images'] as List? ?? []),
      rating: (d['rating'] as num? ?? 0.0).toDouble(),
      reviewCount: d['reviewCount'] as int? ?? 0,
      pricePerHour: (d['pricePerHour'] as num? ?? 0).toDouble(),
      priceWeekday: (d['priceWeekday'] as num?)?.toDouble(),
      priceWeekend: (d['priceWeekend'] as num?)?.toDouble(),
      sports: List<String>.from(d['sports'] as List? ?? []),
      amenities: List<String>.from(d['amenities'] as List? ?? []),
      ownerUid: d['ownerUid'] as String? ?? '',
      ownerName: d['ownerName'] as String? ?? '',
      ownerPhone: d['ownerPhone'] as String? ?? '',
      openTime: d['openTime'] as String? ?? '06:00',
      closeTime: d['closeTime'] as String? ?? '22:00',
      slotDurationMinutes: d['slotDurationMinutes'] as int? ?? 60,
      numberOfGrounds: d['numberOfGrounds'] as int? ?? 1,
      sportPricing: (d['sportPricing'] as Map<String, dynamic>?)
          ?.map((k, v) => MapEntry(k, (v as num).toDouble())),
      isVerified: d['isVerified'] as bool? ?? false,
      isActive: d['isActive'] as bool? ?? true,
      contactPhone: d['contactPhone'] as String?,
    );
  }

  Map<String, dynamic> toFirestore() => {
        'name': name,
        'description': description,
        'location': location,
        'address': address,
        'city': city,
        'latitude': latitude,
        'longitude': longitude,
        'images': images,
        'rating': rating,
        'reviewCount': reviewCount,
        'pricePerHour': pricePerHour,
        'priceWeekday': priceWeekday,
        'priceWeekend': priceWeekend,
        'sports': sports,
        'amenities': amenities,
        'ownerUid': ownerUid,
        'ownerName': ownerName,
        'ownerPhone': ownerPhone,
        'openTime': openTime,
        'closeTime': closeTime,
        'slotDurationMinutes': slotDurationMinutes,
        'numberOfGrounds': numberOfGrounds,
        'sportPricing': sportPricing,
        'isVerified': isVerified,
        'isActive': isActive,
        'contactPhone': contactPhone,
        'updatedAt': FieldValue.serverTimestamp(),
      };
}
