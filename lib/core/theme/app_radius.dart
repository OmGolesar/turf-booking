/// TurfX border radius tokens.
///
/// Consistent corner radius values across the application.
class AppRadius {
  AppRadius._();

  /// 4px - Subtle rounding (chips, tags)
  static const double xs = 4.0;

  /// 8px - Small rounding (buttons, inputs)
  static const double sm = 8.0;

  /// 12px - Medium rounding (cards)
  static const double md = 12.0;

  /// 16px - Large rounding (bottom sheets, modals)
  static const double lg = 16.0;

  /// 20px - Extra large rounding (featured cards)
  static const double xl = 20.0;

  /// 24px - XXL rounding (image containers)
  static const double xxl = 24.0;

  /// Full circle (avatars, FABs)
  static const double full = 999.0;
}
